SELECT
	M.TITLE
	,T.RENTAL_DATE
	,T.RENTAL_ID
	,I.DVD_FLAG
	,I.VIDEO_FLAG
FROM
	TRANSACTION T
		LEFT JOIN MOVIE M
		ON T.MOVIE_ID = M.MOVIE_ID
			LEFT JOIN INVENTORY I
			ON T.RENTAL_ID = I.RENTAL_ID
WHERE
	DVD_FLAG = 'Y'
	AND T.RENTAL_DATE > CURRENT_DATE - INTERVAL '60' DAY
ORDER BY
	T.RENTAL_DATE